package profiler;

public class Position {

  private double x;

  /**
   * Constructs a new Position object
   * @param x the x location of this position
   */
  public Position(double x) {
    this.x = x;
  }

  /**
   * Returns the x location of this position
   * @return the x location of this position
   */
  public double getX() {
    return this.x;
  }

}
